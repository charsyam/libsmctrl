#!/bin/bash
# Scan TMD offsets by running tmd_test_one for each candidate
set -euo pipefail

DEV=${1:-0}
BIN="./tmd_test_one"

echo "=== Getting baseline ==="
BASELINE=$(timeout 5 $BIN $DEV 0 0 --baseline 2>/dev/null || echo "-1")
echo "Baseline: $BASELINE"
BASELINE_COUNT=$(echo "$BASELINE" | awk '{print $1}')
echo ""

echo "=== Scanning candidate offsets ==="
# Test offsets: focus on likely regions based on TMD dump
for off in \
    0x054 0x058 0x05c \
    0x080 0x084 0x088 0x08c 0x090 0x094 0x098 0x09c \
    0x0a0 0x0a4 0x0a8 0x0ac 0x0b0 0x0b4 0x0b8 0x0bc \
    0x0c0 0x0c4 0x0c8 0x0cc 0x0d0 0x0d4 0x0d8 0x0dc \
    0x0e0 0x0e4 0x0e8 0x0ec 0x0f0 0x0f4 0x0f8 0x0fc \
    0x100 0x104 0x108 0x10c 0x110 0x114 0x118 0x11c \
    0x120 0x124 0x128 0x12c 0x130 0x134 0x138 0x13c \
    0x140 0x144 0x148 0x14c 0x150 0x154 0x158 0x15c \
    0x160 0x164 0x168 0x16c 0x170 0x174 0x178 0x17c \
    0x180 0x184 0x188 0x18c 0x190 0x194 0x198 0x19c \
    0x1a0 0x1a4 0x1a8 0x1ac 0x1b0 0x1b4 0x1b8 0x1bc \
    0x1c0 0x1c4 0x1c8 0x1cc 0x1d0 0x1d4 0x1d8 0x1dc \
    0x1e0 0x1e4 0x1e8 0x1ec 0x1f0 0x1f4 0x1f8 0x1fc \
    0x200 0x204 0x208 0x20c 0x210 0x214 0x218 0x21c \
    0x220 0x224 0x228 0x22c 0x230 0x234 0x238 0x23c \
    0x240 0x244 0x248 0x24c 0x250 0x254 0x258 0x25c \
    0x260 0x264 0x268 0x26c 0x270 0x274 0x278 0x27c \
    0x280 0x284 0x288 0x28c 0x290 0x294 0x298 0x29c \
    0x2a0 0x2a4 0x2a8 0x2ac 0x2b0 0x2b4 0x2b8 0x2bc \
    0x2c0 0x2c4 0x2c8 0x2cc 0x2d0 0x2d4 0x2d8 0x2dc \
    0x2e0 0x2e4 0x2e8 0x2ec 0x2f0 0x2f4 0x2f8 0x2fc \
    0x300 0x304 0x308 0x30c 0x310 0x314 0x318 0x31c \
    0x320 0x324 0x328 0x32c 0x330 0x334 0x338 0x33c \
    0x340 0x344 0x348 0x34c 0x350 0x354 0x358 0x35c \
; do
    for mode in 1 2 3; do
        RESULT=$(timeout 3 $BIN $DEV $off $mode 2>/dev/null || echo "-1")
        COUNT=$(echo "$RESULT" | awk '{print $1}')
        if [ "$COUNT" != "-1" ] && [ "$COUNT" -le 4 ] 2>/dev/null; then
            echo "** HIT ** off=$off mode=$mode: $RESULT"
        elif [ "$COUNT" != "-1" ] && [ "$BASELINE_COUNT" != "-1" ] && [ "$COUNT" -lt $((BASELINE_COUNT / 2)) ] 2>/dev/null; then
            echo "* partial * off=$off mode=$mode: $RESULT"
        fi
    done
done

echo ""
echo "=== Scan complete ==="
